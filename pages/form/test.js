import Head from 'next/head';

export default function Home({ sheetdata }) {
  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <div className="max-w-xl mb-2 flex items-center justify-center m-auto p-4">
          {sheetdata.map((item) => (
            <div>
              <p className="text-justify mb-2 font-semibold">{item[2]}</p>
              <p className="text-justify mb-2">{item[3]}</p>
              <p className="text-justify mb-2">{item[4]}</p>
              <p className="text-justify mb-2">{item[5]}</p>
              <p className="text-justify mb-2">{item[6]}</p>
              <p className="text-justify mb-2">{item[7]}</p>
            </div>
          ))}
        </div>
      </main>
    </div>
  );
}

export async function getServerSideProps() {
  const req = await fetch('https://ruleslb3r.vercel.app/api/sheet');
  const res = await req.json();

  return {
    props: {
      sheetdata: res.data,
    },
  };
}
